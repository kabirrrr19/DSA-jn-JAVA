package sllNumbers;

public class NodeSLL {
	
	private int num;
	private NodeSLL next;
	
	public NodeSLL(int num) {
		this.num = num;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public NodeSLL getNext() {
		return next;
	}

	public void setNext(NodeSLL next) {
		this.next = next;
	}
	
	
	
}
